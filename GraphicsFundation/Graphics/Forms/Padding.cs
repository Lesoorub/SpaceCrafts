namespace GraphicsFundation.Graphics.Forms
{
    public struct Padding
    {
        public float Left;
        public float Top;
        public float Right;
        public float Bottom;

        public Padding(float left, float top, float right, float bottom)
        {
            this.Left = left;
            this.Top = top;
            this.Right = right;
            this.Bottom = bottom;
        }
    }
}
